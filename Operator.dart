void main(List<String> arguments) {
  print(5 + 3);
  print(5 / 2);

  // ~ là lấy phần nguyên
  print(5 ~/ 2);

  // chia lấy dư
  print(5 % 2);

  // Toán tử 3 ngôi

  var a = 5;
  var b = 10;

  print(a < b ? "True" : "False");

  var aNumber = 5;
  var bNumber;

  print(aNumber ?? bNumber);

  // List
  List temp = [1, 12, 23];
  print(temp[2]);
}
