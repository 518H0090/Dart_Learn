void main(List<String> args) {
  // const dùng cho số phải gấn giá trị, phải nhận 1 giá trị cụ thể khi khởi tạo
  const double pi = 3.14562;

  print(pi);

  // final dùng cho text gán giá trị 1 lần duy nhất không được thay đổi , không cần nhận 1 giá trị cụ thể khi khởi tạo
  final String valueString;

  valueString = "Key Value";

  print(valueString);
}
